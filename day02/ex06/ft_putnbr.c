void	ft_putchar(char c);

void	putnbr(int n)
{
	if (n < 0)
	{
		ft_putchar('-');
		n = -n;
	}
	if (n / 10)
		putnbr(n / 10);
	ft_putchar(n % 10 + '0');
}

void	ft_putnbr(int n)
{
	if (n != -2147483648)
	{
		putnbr(n);
	}
	else
	{
		putnbr(-214748364);
		putnbr(8);
	}
}
