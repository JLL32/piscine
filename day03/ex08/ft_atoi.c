int	ft_atoi(char *str)
{
	int n;
	int sign;

	sign = 1;
	while (*str == '-' || *str == '+' || *str <= 32)
	{
		if (*str == '-')
		{
			sign = -sign;
			str++;
		}
		else if (*str == '+')
			str++;
		else if (*str <= 32)
			str++;
	}
	n = 0;
	while (*str >= 48 && *str <= 57)
	{
		n = n * 10 + *str - 48;
		str++;
	}
	return (sign * n);
}
